class geoLocation:

    x = 0.0
    y = 0.0
    z = 0.0

    def __init__(self,x=0.0,y=0.0,z=0.0):
        self.x = x
        self.y = y
        self.z = z

    def x(self) -> int:
        return self.x

    def y(self) -> int:
        return self.y

    def z(self) -> int:
        return self.z


