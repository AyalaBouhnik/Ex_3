from geoLocation import geoLocation


class IgeoLocation(geoLocation):
    def __init__(self):
        self.x=0
        self.y = 0
        self.z = 0

    def x(self) -> int:
        return self.x()

    def y(self) -> int:
        return self.y()

    def z(self) -> int:
        return self.z()